package model.contact;

public class Intervenants {
	private int typeIntervenant;
	private int noIntervenant;
	private String nom;
	private String adresse;
	private String telephone;
	private String codePostal;
	private String ville;
	private String province;
	private String pay;
	private String email;
	public int getTypeIntervenant() {
		return typeIntervenant;
	}
	public void setTypeIntervenant(int typeIntervenant) {
		this.typeIntervenant = typeIntervenant;
	}
	public int getNoIntervenant() {
		return noIntervenant;
	}
	public void setNoIntervenant(int noIntervenant) {
		this.noIntervenant = noIntervenant;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getAdresse() {
		return adresse;
	}
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getCodePostal() {
		return codePostal;
	}
	public void setCodePostal(String codePostal) {
		this.codePostal = codePostal;
	}
	public String getVille() {
		return ville;
	}
	public void setVille(String ville) {
		this.ville = ville;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
	

}
